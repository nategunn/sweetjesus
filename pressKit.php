    <h3>Bio</h3>
  	
    <div class="bioBody">
        <p>Though <i>S​hortwave Oracle​</i> is a debut LP, it’s not the first time we’ve heard from Sweet Jesus. In the band’s first iteration, in the mid­-2000s, singer/guitarist​ <b>Bobby Malvestuto​</b> and drummer <b>C​hris “Crystal” Pistols</b> crafted a blues­ and­ folk­ influenced rock sound that made an impression on Seattle and brought the band attention from <b>K​EXP</b>.​Yet it wasn’t until the band’s founders joined forces with bassist <b>A​ustin​ R​ood​</b> and lead guitarist <b>R​yan ​F​itzsimmons</b> ​in 2011 that it truly found its footing and “colored in” its sound. “H​aving Ryan and Austin in the band has really just magnified and transcended what Chris and I had started,” says Malvestuto. Now as a four­-piece, Sweet Jesus have expanded their sound to incorporate folk punk, psychedelic blues, <b>S​tephen Malkmus</b>­​ slacker rock, with subtle nods to 80’s power­pop.
        </p> 
        
        <p>“Getting Over,” which occupies the second slot on the band’s debut album <i>S​hortwave Oracle</i>,​ helps to tell the story of its own existence. “I had a few pressing themes on my head, while writing these tracks. The death of my father, who died 5 years ago. Lost love, disillusionment and the desperation of getting older.” Propelled by Rood’s distorted bass playing, Malvestuto sings, “I used to sleep all day, I used to never be afraid, Just like my father lying in his grave.” Vibrant, dirty, and energetic, the song serves as the perfect introduction to the new and revitalized version of Sweet Jesus.</p>

    	<p>Opening track, “Aftermath,” takes the folk­rock beginnings of the band and blends it with angular and mounting guitars, ending in a psychedelic haze. “Ghost Lady” is a blues­rock scorcher with an <b>E​lvis Costello</b>​ bop, while culminating track, “Psychic Bohemia,” is a slow­burner that chugs and twangs towards a dejected march. After much ruminating and self­ reflection throughout the album, Malvestuto assuredly sings, “You know the reason,” as his epiphany is washed with the buzz of a shortwave radio.</p>

    	<p>On <i>S​hortwave Oracle</i>,​ recorded by <b>B​en ​J​enkins</b> ​at the <b>K​ill​ R​oom</b> ​Studios, the band attacks rock n roll, folk punk, psychedelic blues on songs that deal with death, social media panic, and lost love. Now rejuvenated after a long creative journey, Sweet Jesus is excited to share their debut album and spread their music beyond their Pacific Northwest roots.</p>
